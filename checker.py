# Абстрактная вычислительная машина имеет один регистр и шесть инструкций:
#   LD A - загрузить операнд A в регистр
#   ST A - выгрузить регистр в переменную A
#   AD A - прибавить A к регистру
#   SB A - вычесть A из регистра
#   ML A - умножить регистр на A
#   DV A - разделить регистр на A
# Дано выражение в постфиксной форме из однобуквенных операндов и операций
# +, -, *, /. Вывести инструкции машины для вычисления выражения. Результат
# остаётся в регистре, для временных переменных используются имена Tn.

import os


class Checker:
    """Проверки ввода: файл существует и не пустой, выражение корректное."""

    SIGNS = "+-*/"

    @staticmethod
    def check_file(path):
        if not os.path.exists(path):
            raise ValueError("файл input.txt не найден")
        if os.path.getsize(path) == 0:
            raise ValueError("файл input.txt пустой")

    @staticmethod
    def validate(expr):
        if expr == "":
            raise ValueError("пустая строка")

        # depth это сколько чисел лежало бы в стеке при вычислении
        # буква кладёт одно число, операция снимает два и кладёт одно
        # если перед операцией чисел меньше двух - не хватает операндов
        # если в конце осталось не одно число - лишний операнд
        depth = 0
        for pos, ch in enumerate(expr, 1):
            if "A" <= ch <= "Z" or "a" <= ch <= "z":
                depth += 1
            elif ch in Checker.SIGNS:
                if depth < 2:
                    raise ValueError(
                        f"операции '{ch}' на позиции {pos} "
                        "не хватает операндов"
                    )
                depth -= 1
            else:
                raise ValueError(
                    f"недопустимый символ '{ch}' на позиции {pos}"
                )

        if depth != 1:
            raise ValueError(
                "лишние операнды, выражение не сводится к одному числу"
            )
